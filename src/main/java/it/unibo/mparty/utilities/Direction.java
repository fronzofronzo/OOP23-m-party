package it.unibo.mparty.utilities;

/**
 * This enum is used by the {@link GameBoard} to indicate
 * the possible directions to move on the board.
 */
public enum Direction {
    /**
     * upward.
     */
    UP,
    /**
     * downward.
     */
    DOWN,
    /**
     * rightward.
     */
    RIGHT,
    /**
     * leftward.
     */
    LEFT;
}
